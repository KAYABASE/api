<?php

namespace App\Notifications;

use App\Services\Notification\NotificationMessage;
use App\Services\Notification\NotificationTemplateOptions;
use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;
use Illuminate\Support\HtmlString;

class SendNotificationTemplate extends Notification implements ShouldQueue
{
    use Queueable;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct(public NotificationTemplateOptions $options)
    {
        //
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return $this->options->via($notifiable);
    }


    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return NotificationMessage::make($notifiable)
            ->title($this->options->template->title)
            ->body($this->options->template->body)
            ->toArray();
    }
}
